import pandas as pd
from sklearn.linear_model import LinearRegression

# -----------------------------------
# 1. CSV 파일 읽기
# -----------------------------------
df = pd.read_csv("product_fixed.csv")

print("===== 데이터 =====")
print(df)

# -----------------------------------
# 2. 물품을 숫자로 변환
# -----------------------------------
item_mapping = {
    "키보드": 0,
    "마우스": 1,
    "캠": 2,
    "마우스패드": 3
}

df["물품코드"] = df["물품"].map(item_mapping) #딕셔너리에 map()함수를 이용하여 값을 숫자로 변환
                                            #하여 새로운 컬럼에 수록
                                            #키보드 가 0, 마우스 1, 캠 2, 마우스패드 3
                                            #으로 저장
print(df)

# -----------------------------------
# 3. 모델 학습 준비
# -----------------------------------
# X: 수량만 사용
X = df[["수량"]]

# y: 금액
y = df["금액"]

# -----------------------------------
# 4. 모델 생성 및 학습
# -----------------------------------
model = LinearRegression()
model.fit(X, y)

# -----------------------------------
# 5. 결과 확인
# -----------------------------------
print("\n기울기:", model.coef_[0])
print("절편:", model.intercept_)

# -----------------------------------
# 6. 단가 가져오기 함수
# -----------------------------------
price_table = {
    "키보드": 15000,
    "마우스": 12000,
    "캠": 25000,
    "마우스패드": 5000
}

# -----------------------------------
# 7. 사용자 입력 및 예측
# -----------------------------------
while True:
    print("\n물품 선택: 키보드 / 마우스 / 캠 / 마우스패드")
    item = input("물품 입력 (종료:q): ")

    if item == 'q':
        print("프로그램 종료")
        break

    if item not in price_table:
        print("잘못된 물품입니다.")
        continue

    try:
        qty = int(input("수량 입력: "))

        if qty <= 0:
            print("수량은 1 이상 입력하세요.")
            continue

        # -----------------------------------
        # 8. 예측
        # -----------------------------------
        predicted_amount = model.predict([[qty]])[0] #가격행, 열(0)의 값을 가지고 옴
                                                    #리스트는 행, 열로 가지고 와야 함.

        # 실제 금액 (정답)
        real_amount = qty * price_table[item]

        # -----------------------------------
        # 9. 결과 출력
        # -----------------------------------
        print("\n===== 결과 =====")
        print(f"물품: {item}")
        print(f"수량: {qty}")
        print(f"예상 금액 (ML): {predicted_amount:.2f}")
        print(f"실제 금액     : {real_amount}")

    except:
        print("숫자를 입력하세요.")